                    <p>
                        The <strong>CPSU Document Tracking System</strong> is a digital platform developed to streamline
                        and enhance the processing and monitoring of official documents within the <strong>Central
                            Philippines State University (CPSU)</strong>.
                        This user-centered system promotes efficiency, transparency, and accountability in handling
                        university communications and administrative documents.
                    </p>

                    <p>
                        Designed with automation in mind, the system enables documents to be routed directly to the
                        appropriate personnel or department for necessary action. Whether for review, approval, or
                        signature, documents are digitally tagged and assigned, reducing delays and manual follow-ups.
                    </p>

                    <p><strong>Key Features:</strong></p>
                    <ul>
                        <li><strong>Automated Routing:</strong> Documents are forwarded to the designated recipients
                            based on workflow logic, ensuring timely handling and accountability.</li>
                        <li><strong>Document Tracking Slip:</strong> A digital tracking slip is generated for every
                            submission, which records critical metadata such as source, destination, timestamps,
                            remarks, and status updates.</li>
                        <li><strong>Real-Time Monitoring:</strong> Users can monitor the document's path, review
                            progress, and identify bottlenecks through an intuitive dashboard interface.</li>
                        <li><strong>Email Notifications:</strong> Users receive automated email alerts through their
                            CPSU institutional email accounts to stay updated on document activity and required actions.
                        </li>
                        <li><strong>Audit-Friendly:</strong> The system keeps a detailed activity log that serves as an
                            official record for internal reviews, audits, and accreditation needs.</li>
                        <li><strong>Secure & Centralized:</strong> All data is securely stored and maintained by the
                            CPSU Records Office, adhering to institutional and legal standards.</li>
                    </ul>

                    <p>
